public class Horse {
    private String horseName;
    private String horseColour;
    private int horseYear;

    public String getHorseName() {return horseName;}
    public String getHorseColour() {return horseColour;}
    public int getHorseYear() {return horseYear;}

    public void setHorseName(String horseName) {
        this.horseName = horseName;
    }

    public void setHorseColour(String horseColour) {
        this.horseColour = horseColour;
    }

    public void setHorseYear(int horseYear) {
        this.horseYear = horseYear;
    }
}
